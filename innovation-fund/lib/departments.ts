export function collegesFor(campus?: string): string[] {
  if (campus && campus in KNU) return Object.keys(KNU[campus as Campus]);
  return [];
}
export function deptsFor(campus?: string, college?: string): string[] {
  if (campus && campus in KNU && college && KNU[campus as Campus][college]) return KNU[campus as Campus][college];
  return [];
}
// 저장된 학과명으로 소속 단과대학 찾기 (편집 시 드롭다운 복원용)
export function collegeOfDept(campus?: string, dept?: string): string {
  if (!campus || !(campus in KNU) || !dept) return "";
  const m = KNU[campus as Campus];
  for (const c of Object.keys(m)) if (m[c].includes(dept)) return c;
  return "";
}
