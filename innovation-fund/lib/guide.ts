export const GUIDE_KEY = "guide";

export interface GuideSection { id: string; title: string; html: string; }
export interface GuideConfig { sections: GuideSection[]; updatedAt?: string; }

function gid(): string { return "g-" + Math.random().toString(36).slice(2, 9); }

export function normalizeGuide(v: unknown): GuideConfig | null {
  if (!v || typeof v !== "object") return null;
  const o = v as Record<string, unknown>;
  if (!Array.isArray(o.sections)) return null;
  const sections: GuideSection[] = (o.sections as unknown[])
    .filter((s): s is Record<string, unknown> => !!s && typeof s === "object")
    .map((s) => ({ id: String(s.id || gid()), title: String(s.title || "").trim(), html: String(s.html || "") }))
    .filter((s) => s.title || s.html);
  return { sections, updatedAt: o.updatedAt ? String(o.updatedAt) : undefined };
}

